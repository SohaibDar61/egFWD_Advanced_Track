/******************************************************************************   
* \Description     : Turn on the LED                                  
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : parameterName   Parameter Describtion                     
* \Parameters (out): None                                                      
* \Return value:   : Std_ReturnType  E_OK  OR E_NOT_OK                                  
*******************************************************************************/
void LED_TURN_ON(uint8_t ID)
{
	if(ID == RED_LED)
    {
        Dio_WriteChannel(PORTF,PIN1,STD_HIGH);     
        Dio_WriteChannel(PORTF,PIN2,STD_LOW);
        Dio_WriteChannel(PORTF,PIN3,STD_LOW);

    }
	else if(ID == BLUE_LED)
    {
        Dio_WriteChannel(PORTF,PIN1,STD_LOW);
        Dio_WriteChannel(PORTF,PIN2,STD_HIGH);
        Dio_WriteChannel(PORTF,PIN3,STD_LOW);

    }
    else 
    {
        Dio_WriteChannel(PORTF,PIN1,STD_LOW);
        Dio_WriteChannel(PORTF,PIN2,STD_LOW);
        Dio_WriteChannel(PORTF,PIN3,STD_HIGH);
    }
}

/******************************************************************************
* \Description     : Describe this service                                                                                                 
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : ID                      
* \Parameters (out): None                                                      
* \Return value:   : void                                 
*******************************************************************************/
void LED_TURN_OFF(uint8_t ID)
{
    if(ID == RED_LED)
    {
        Dio_WriteChannel(PORTF,PIN1,STD_LOW);
    }
    else if(ID == BLUE_LED)
    {
        Dio_WriteChannel(PORTF,PIN2,STD_LOW);
    }
    else 
    {
        Dio_WriteChannel(PORTF,PIN3,STD_LOW);
    }
}

